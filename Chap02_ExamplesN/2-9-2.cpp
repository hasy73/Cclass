/* 這是使用cin輸入的程式範例 */
#include <iostream>
using namespace std;
int main()
{
    int i;
    char ch;
    float j;
    cout<<"Input a integer a character and a floating point:";  
    cin>>i>>ch>>j;
    cout<<"The integer you give us is "<<i<<endl;
    cout<<"The character you give us is "<<ch<<endl;
    cout<<"The floating point you give us is "<<j<<endl;
    system("pause");
    return 0;
} 
