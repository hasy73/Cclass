#include <stdio.h>
#include<stdlib.h>
int main(void)
{
  char ch='c';  //宣告字元變數ch，並指定值為c
  int i=1;      //宣告整數變數i，並指定值為1
  float j=2.0;   //宣告浮點數整數j，並指定值為2.0
  printf("This program show how to print variables\n");
  printf("ch = %c\n",ch); //以字元型態印出變數ch
  printf("i = %d\n",i);   //以整數型態印出變數i
  printf("j = %f\n",j);   //以浮點數型態印出變數j
  system("pause");     //使程式暫停在執行畫面讓我們看到結果
} 

